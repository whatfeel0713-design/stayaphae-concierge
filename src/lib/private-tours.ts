export function renderPrivateToursForPrompt(): string {
  const belt = ISLAND_BELT.map((island) => `- ${island.name}: ${island.note}`).join("\n");
  const tours = PRIVATE_TOURS.map(
    (tour) =>
      `- [${tour.duration}] ${tour.title} (${tour.islands})\n  ` +
      tour.highlights.join(" / "),
  ).join("\n");

  return [
    "## 프라이빗 투어 — 다리로 이어진 섬들",
    belt,
    "",
    "## 일정별 투어 제안 (반나절 ~ 3박 4일)",
    tours,
    "",
    "주의: 렌터카·기사·보트·승선권은 아직 자동 예약이 연결돼 있지 않다. " +
      "구체적인 일정을 원하는 손님에게는 위 코스를 뼈대로 제안하되, 실제 섭외는 " +
      "문의 이메일로 이어가도록 안내할 것.",
  ].join("\n");
}
